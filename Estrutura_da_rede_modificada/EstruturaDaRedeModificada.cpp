#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main(int argc, char const *argv[])
{
    float w1[3][3];
    w1[0][0]=20;
    w1[1][0]=30;
    w1[2][0]=-10;

    w1[0][1]=-30;
    w1[1][1]=-50;
    w1[2][1]=60;
    
    float w2[3][3];
    w2[0][0]=20;
    w2[1][0]=40;
    w2[2][0]=-50;

    float E1,E2;

    printf("\nValor E1: ");
    scanf("%f",&E1);
    printf("\nValorE2: ");
    scanf("%f",&E2);

    float I[3][3];
    I[0][0]=E1;
    I[0][1]=E2;
    I[0][2]=1.0;

    float O[3][3];
    O[0][0]=I[0][0];
    O[0][1]=I[0][1];
    O[0][2]=I[0][2];
    
    I[1][0]= O[0][0] * w1[0][0] + O[0][1] * w1[1][0] + O[0][2] * w1[2][0];
    I[1][1]= O[0][0] * w1[0][1] + O[0][1] * w1[1][1] + O[0][2] * w1[2][1];
    I[1][2]= 1.0;

    O[1][0]=1.0/(1.0+exp(-I[1][0]));
    O[1][1]=1.0/(1.0+exp(-I[1][1]));
    O[1][2]=I[1][2];

    I[2][0]=O[1][0] * w2[0][0] + O[1][1] * w2[1][0] + O[1][2] * w2[2][0];
    O[2][0]=1.0/(1.0+exp(-I[2][0]));

    printf("\nI[2][0]: %f\nO[2][0]: %f",I[2][0],O[2][0]);

    return 1;
}
